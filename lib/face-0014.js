function fixFace0014(type, s) {
    if (!s) return s;
    return s.replace(/\u0014[A-Za-z0-9^]/gm, rs => {
        const k = rs.replace(/\u0014/g, '');
        const code = k.charCodeAt(0) - 71;
        const alias = LIST[code];
        switch (type) {
            case 'html':
                return `<img class="s60_qq_emoji" src="${config.faceWebPublicDir}/${encodeURIComponent(
                    alias,
                )}.gif" alt="${alias}" title="${alias}" />`;
            case 'content':
                return `[${alias}]`;
            default:
                throw new Error('unknown Type');
        }
    });
}

